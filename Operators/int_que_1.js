let a = 12228979083508370849

console.log(a%10); //returns the last digit of the number, which is 9 in this case.

console.log(a%100); //returns the last two digits of the number, which is 49 in this case.

console.log(a%1000); //returns the last three digits of the number, which is 849 in this case.

//this is a common technique to extract the last n digits of a number using the modulus operator with powers of 10. 



