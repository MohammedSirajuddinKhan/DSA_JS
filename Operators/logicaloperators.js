console.log(10<5); // gives answer in true or false
console.log(10>=5); // gives answer in true or false
console.log(10<=5); // gives answer in true or false
console.log(10!=5); // gives answer in true or false
console.log(10==5); // gives answer in true or false
console.log(10===5); // gives answer in true or false
console.log(10!==5); // gives answer in true or false


// && || ! (AND, OR, NOT) used to check multiple conditions together

